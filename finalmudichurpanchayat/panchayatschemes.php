<p>Our key environmental interventions are in the areas of solid waste management, natural resource management and environmental advocacy.The solid waste management projects of Hand in Hand India strive for maximum waste recovery through composting, recycling, and reuse, and aims at minimizing the waste being dumped in the open dump yards. Through the process, it also offers continuous employment to individuals from socially and economically
disadvantaged groups. The project also ensures that the practices are implemented in a sustainable manner through community participation. The long-term objective is thus to reduce the environmental degradation caused by the unscientific handling of solidv waste.
 <center> <img class="subimage"src="images/panchayat schemes/21.png"></center>
The representatives projected an overall strategy and idea for handling waste at Mudichur. After series of meeting and consensus with village people, the project was given to Hand in Hand India/ SEED Trust.The SWM project was launched in March 2007 in Mudichur. The required waste management equipment like segregation packs, gloves, tricycles etc. were bought with the small fund managed within. Soon after, the awareness programmes and campaigns were organized to
sensitize the waste management benefits. Hand in Hand India / SEED Trust are responsible for managing human resource, knowledge and education and financial aids. The project initially started with 700 households of 7 wards. Gradually, the operation is now exp anded to 4763 households in Total 12 wards including commercial establishments. Mudichur is the first project initiative taken by Hand in Hand India / SEED Trust which has led to successful other projects across India. The model is replicated in various parts of Chennai and across India.</p>

<p align="left"class="ptitle"><b>R O Water Plant in Mudichur Panchayat</b></p>
<center> <img class="subimage"src="images/panchayat schemes/23.png"></center>
<p>The recurring expenses of the Solid Waste Management Project at Mudichur village panchayat had been met out with the support of beneficiary contribution from the general public. However there is a shortage of over Rs. 25,000/- per month. In an effort to meet out the shortage and considering annual increase in the recurring expenses, it has been proposed by Hand in Hand Inclusive Development and Services (HHIDS) to install a 4000 litres per hour Reverse Osmosis Plant at Mudichur.</p>
<p>HHIDS will work along with MudichurPanchayat to:</p>
<ol type="1">
<li> Provide the residents of Mudichur Village panchayat with potable drinking water conforming to WHO and BIS Standards.</li>
<li>Provide packaged drinking water at half the cost of existing market prices.</li>
<li> Support the Solid Waste Management project with the revenues from the project.</li></ol>
<p>HHIDS will operate and run the plant for a period of 4 years to recover its investment before handing over the plant and its operation to the panchayat.</p>
<h3>Survey and Scope for the Project:</h3>
<p>During a survey at Mudichur, to study the water quality and water consumption patterns, the following was noted:</p>
<ol type="1">
<li> The major sources of drinking water for the residents were 20 liter water cans from private suppliers, pipeline connection provided by the panchayat, open wells and bore wells.
</li>
<li>The water test also returned results which indicated that parameters like TDS, Total hardness, alkalinity and Total Coliform bacteria were well above the desirable limits.
</li></ol>
<p align="left"class="ptitle"><b>Operations:</b></p>
<p>Based on a proposal submitted by HHIDS to the Mudichurpanchayat, a resolution was passed in the GramaSabha meeting in 2011 and an agreement was signed between the two parties. Under this agreement, HHIDS would invest the necessary capital for machinery as well as daily operations of the plant. HHIDS was also authorized to distribute water to the households for a fixed rate. MudichurPanchayat had to provide a rent-free building along with an electricity connection and a permanent source of water.</p>
<p>HHIDS has invested close to Rs. 33,00,000/- towards:</p>
<ol type="1">
<li> Construction of an open well (30 feet depth by 30 feet diameter)</li>
<li> Procuring and installation of a 4000 liters per hour RO plant along with the necessary storage, treatment and cleaning systems.</li>
<li> Procurement and installation of a submersible motor to pump water from the well to the RO plant.</li>
<li> Modification of the panchayat contributed building to house the RO plant.</li></ol>
<p>The installed RO plant has the following treatment systems to ensure water quality:</p>
<ol type="1">
<li> A degasser tower for aeration of the raw water from the well.</li>
<li> Activated Carbon filter</li>
<li> Sand Filter</li>
<li> Micron Filters</li>
<li> RO membranes</li>
<li> UV Sterilization</li>
<li> Ozone Treatment.</li></ol>
<p>HHIDS has also set-up a small lab to test the basic water quality parameters every day.</p>
<p>20 liter water cans are supplied to the households at Rs 15 per can to recover the capital as well as recurring expenses.</p>
<h3>Mode of Operations and Investment Recovery Plan:</h3>
<p>HHIDS has identified distributors in each of the 12 wards of Mudichur to increase the efficiency of door to door distribution of the water cans. HHIDS supplies nearly 80-100 cans every day to all the distributors who then supply to the households. Two dedicated vehicles have been hired to ensure that cans are delivered to the distributors every day.
HHIDS supplies the water to the distributors at Rs. 11 per can. The distributors then supply the cans to households at Rs.15 allowing them to make an additional income every month. Currently, HHIDS is supplying nearly 800 cans per day across all wards of Mudichur. Operations are being scaled up to reach a monthly target of 1100 cans per day which will ensure recovery of the investment within the estimated time-period.</p>
